             <p><strong><u>Name of the Course: </u></strong></p>
          <p><strong>Diploma in nursing Science and Midwifery</strong></p>
          <p><strong>Course Duration: 3 Years</strong></p>
          <p><strong><u>Admission Criteria for both local and foreign students.</u></strong></p>
          <p>The applicant who meets the following criteria will be eligible to enter into the <strong>Diploma in nursing Science and Midwifery</strong> course.</p>
          <p><strong> 1. <u>Eligibility of admission for the Local students</u></strong></p>
          <ul>
          <li>The applicant must be the citizen of Bangladesh.</li>
          <li>The applicants have to be passed both SSC and HSC or equivalent examinations from any group in total CGPA 6.00 with at least GPA 2.50 either SSC or HSC or equivalent examination.</li>
          <li>The applicant should apply within 02 (Two) years of passing HSC or equivalent Examination following SSC or equivalent examination as continual.</li>
          <li><strong>Applicant must be seated for the admission test examination arranged by BNC and have to get at least 40% marks.</strong></li>
          </ul>
          <p><strong>2. <u>Eligibility of admission for the Foreign Students</u></strong></p>
          <ul>
          <li>The applicants must need Cumulative Grading Point Average(CGPA) both O’level and A’level or equivalent examinations from any group in total 6.00 but not less than CGPA 2.50 in either O’level or A’level examination.</li>
          <li>The applicant should apply within 02 (Two) years of passing A’level or equivalent Examination following O’level or equivalent examination as continual.</li>
          <li>Students who meet up mentioned criteria above, they will get the opportunity to study in any non-government nursing college in Bangladesh. One-third of total seat will reserve for foreign students. If not found, Bangladeshi students can be admitted with the vacant seats.</li>
          <li>Foreign students must be attested their application form, O’level and A’level or equivalent certificate and transcript by their Ministry of Foreign Affairs through Bangladesh embassy and submitted to the Ministry of Foreign Affairs of Bangladesh in time. Ministry of Foreign Affairs will send it to Director General, Nursing and Midwifery council. With permission of the Health education and family welfare division, Bangladesh Nursing and Midwifery council will take necessary steps to equalize marks and others criteria. Ministry of Foreign Affairs will permit to admit with mark equalize report from the Council.</li>
          </ul>
          <p><strong>3. <u>Student Registration:</u></strong></p>
          <p>Students have to be registered after admission as per BNMC rules and regulation.</p>
          <p><strong>4. <u>Medical Fitness:</u></strong></p>
          <p>Successful candidate must be performed medical fitness check-up before admission.</p>
          <p><strong>Nota Bene:</strong></p>
          <p><strong>An admission Committee will reserve all the right to take any decision.</strong></p>
